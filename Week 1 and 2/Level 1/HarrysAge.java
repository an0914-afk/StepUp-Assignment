public class HarrysAge {
    public static void main(String[] args) {

        // Variables
        int birthYear = 2000;
        int currentYear = 2024;

        // Calculate Age
        int harryAge = currentYear - birthYear;

        // Output
        System.out.println("Harry's age in " + currentYear + " is " + harryAge);
    }
}